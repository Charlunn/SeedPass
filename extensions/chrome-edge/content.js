  function pinInput(onComplete) {
    const wrapper = document.createElement("div");
    const row = document.createElement("div");
    const status = document.createElement("div");
    row.className = "seedpass-pin-row";
    status.className = "seedpass-status";
    const boxes = Array.from({ length: 6 }, () => {
      const box = document.createElement("input");
      box.type = "password";
      box.inputMode = "numeric";
      box.maxLength = 1;
      box.autocomplete = "off";
      box.className = "seedpass-pin-box";
      row.append(box);
      return box;
    });
    wirePinBoxes(boxes, (pin) => onComplete(pin, status, boxes));
    wrapper.append(row, status);
    setTimeout(() => boxes[0]?.focus(), 0);
    return wrapper;
  }

  function wirePinBoxes(boxes, onComplete) {
    boxes.forEach((box, index) => {
      box.addEventListener("input", () => {
        box.value = box.value.replace(/\D/g, "").slice(0, 1);
        if (box.value && index < boxes.length - 1) boxes[index + 1].focus();
        const pin = boxes.map((item) => item.value).join("");
        if (/^\d{6}$/.test(pin)) onComplete(pin);
      });
      box.addEventListener("keydown", (event) => {
        if (event.key === "Backspace" && !box.value && index > 0) boxes[index - 1].focus();
      });
    });
  }

  function positionPanel() {
    if (!panel || !activeInput || !isFillablePasswordInput(activeInput)) return;
    const rect = activeInput.getBoundingClientRect();
    const top = Math.min(rect.bottom + 8, window.innerHeight - panel.offsetHeight - 8);
    const left = Math.min(rect.left, window.innerWidth - panel.offsetWidth - 8);
    panel.style.top = `${Math.max(8, top)}px`;
    panel.style.left = `${Math.max(8, left)}px`;
  }

  function scheduleHide() {
    clearHideTimer();
    hideTimer = setTimeout(() => {
      if (!panel?.contains(document.activeElement)) hidePanel();
    }, 180);
  }

  function clearHideTimer() {
    if (hideTimer) clearTimeout(hideTimer);
    hideTimer = null;
  }

  function hidePanel() {
    clearHideTimer();
    panel?.remove();
    panel = null;
    selectedRecordId = null;
  }

  function textBlock(value) {
    const element = document.createElement("div");
    element.className = "seedpass-text";
    element.textContent = value;
    return element;
  }

  function findPasswordInput() {
    const inputs = Array.from(document.querySelectorAll('input[type="password"]'));
    const visible = inputs.filter(isFillablePasswordInput);
    return visible.find((input) => input === document.activeElement) ?? visible[0] ?? null;
  }

  function isFillablePasswordInput(element) {
    if (!(element instanceof HTMLInputElement) || element.type !== "password") return false;
    const rect = element.getBoundingClientRect();
    const style = window.getComputedStyle(element);
    return (
      rect.width > 0 &&
      rect.height > 0 &&
      style.visibility !== "hidden" &&
      style.display !== "none" &&
      !element.disabled &&
      !element.readOnly
    );
  }

  function fillInput(input, value) {
    if (!input || !value) return;
    const descriptor =
      Object.getOwnPropertyDescriptor(HTMLInputElement.prototype, "value") ??
      Object.getOwnPropertyDescriptor(Object.getPrototypeOf(input), "value");
    descriptor?.set?.call(input, value);
    input.dispatchEvent(new Event("input", { bubbles: true }));
    input.dispatchEvent(new Event("change", { bubbles: true }));
  }

  async function sendMessage(message) {
    const response = await chrome.runtime.sendMessage(message);
    if (!response?.ok) throw new Error(response?.error ?? text[currentLanguage].requestFailed);
    return response;
  }

  function resolveLanguage(value) {
    if (value === "en") return "en";
    if (value === "zh") return "zh";
    return navigator.language?.toLowerCase().startsWith("zh") ? "zh" : "en";
  }
