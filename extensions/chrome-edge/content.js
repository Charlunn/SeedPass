  chrome.runtime.onMessage.addListener((message, _sender, sendResponse) => {
    if (message?.type !== "seedpass-fill") return false;
    const input = activeInput && isFillablePasswordInput(activeInput) ? activeInput : findPasswordInput();
    if (!input) {
      sendResponse({ filled: false });
      return true;
    }
    fillInput(input, message.password);
    hidePanel();
    sendResponse({ filled: true });
    return true;
  });

  document.addEventListener("focusin", (event) => {
    if (!isFillablePasswordInput(event.target)) return;
    activeInput = event.target;
    showSuggestion();
  });

  document.addEventListener("focusout", scheduleHide);
  window.addEventListener("scroll", positionPanel, true);
  window.addEventListener("resize", positionPanel);

  async function showSuggestion() {
    clearHideTimer();
    const state = await sendMessage({ type: "inline-state" }).catch((error) => ({
      ok: false,
      error: error.message
    }));
    currentLanguage = resolveLanguage(state.language);
    panel = panel ?? createPanel();
    renderPanel(state);
    positionPanel();
  }

  function renderPanel(state) {
    const t = text[currentLanguage];
    const title = panel.querySelector("[data-title]");
    const body = panel.querySelector("[data-body]");
    title.textContent = t.title;
    body.innerHTML = "";

    if (!state.ok) {
      body.append(textBlock(state.error || t.requestFailed));
      return;
    }
    if (!state.hasVault) {
      body.append(textBlock(t.noVault));
      return;
    }
    if (!state.records?.length) {
      body.append(textBlock(t.noRecord));
      return;
    }

    body.append(textBlock(state.unlocked ? t.unlocked : t.locked));
    for (const record of state.records) {
      body.append(recordButton(record, state.unlocked));
    }

    if (!state.unlocked) {
      body.append(pinInput(async (pin, status, boxes) => {
        status.textContent = t.unlocking;
        try {
          const response = await sendMessage({
            type: "inline-unlock-and-fill",
            recordId: selectedRecordId ?? state.records[0].id,
            pin,
            timeoutMinutes: state.defaultTimeoutMinutes
          });
          fillInput(activeInput, response.password);
          status.textContent = t.filled;
          setTimeout(hidePanel, 350);
        } catch (error) {
          status.textContent = error.message;
          boxes.forEach((box) => (box.value = ""));
          boxes[0]?.focus();
        }
      }));
    }
  }

  function recordButton(record, unlocked) {
    const t = text[currentLanguage];
    const button = document.createElement("button");
    button.type = "button";
    button.className = "seedpass-record-button";
    const title = record.accountLabel || record.siteIdentity;
    const note = record.note ? ` · ${record.note}` : "";
    const mode = record.mode === "account" ? t.accountMode : t.domainMode;
    button.innerHTML = `<strong></strong><span></span>`;
    button.querySelector("strong").textContent = title;
    button.querySelector("span").textContent = `${mode}${note}`;
    button.addEventListener("click", async () => {
      selectedRecordId = record.id;
      if (!unlocked) {
        panel.querySelector(".seedpass-pin-box")?.focus();
        return;
      }
      try {
        const response = await sendMessage({ type: "inline-fill", recordId: record.id });
        fillInput(activeInput, response.password);
        hidePanel();
      } catch (error) {
        renderPanel({ ok: false, error: error.message });
      }
    });
    return button;
  }

